TextBox::TextBox(float x, float y, float width, float height, const Color& boxColor)
    : Object2D({}, boxColor), _width(width), _height(height), _textureID(0)
{
    setXpos(x);
    setYpos(y);
    setZpos(0.0f);
    _checkCollision = false;

    initVO({});
    compileProg();
    _textureID = generateFont();
}

TextBox::~TextBox()
{
    if (_textureID != 0)
    {
        glDeleteTextures(1, &_textureID);
    }
}

GLuint TextBox::generateFont()
{
    const int cols = 16;
    const int rows = 6;
    const int charW = 8;
    const int charH = 8;
    const int imgW = cols * charW;
    const int imgH = rows * charH;

    std::vector<unsigned char> pixels(imgW * imgH, 0);

    for (int i = 0; i < 96; ++i)
    {
        int gridX = i % cols;
        int gridY = i / cols;

        for (int r = 0; r < charH; ++r)
        {
            unsigned char rowByte = font8x8[i][r];
            for (int c = 0; c < charW; ++c)
            {
                if (rowByte & (1 << (7 - c)))
                {
                    int px = gridX * charW + c;
                    int py = gridY * charH + r;
                    pixels[(imgH - 1 - py) * imgW + px] = 255;
                }
            }
        }
    }

    GLuint tex;
    glGenTextures(1, &tex);
    glBindTexture(GL_TEXTURE_2D, tex);
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RED, imgW, imgH, 0, GL_RED, GL_UNSIGNED_BYTE, pixels.data());

    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);

    return tex;
}

void TextBox::initVO(const std::vector<GLfloat>& vertices)
{
    glGenVertexArrays(1, &_VAO);
    glGenBuffers(1, &_VBO);

    glBindVertexArray(_VAO);
    glBindBuffer(GL_ARRAY_BUFFER, _VBO);
    
    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * 4 * 6 * 500, NULL, GL_DYNAMIC_DRAW);
    
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)(2 * sizeof(float)));
    glEnableVertexAttribArray(1);

    glBindBuffer(GL_ARRAY_BUFFER, 0);
    glBindVertexArray(0);
}

void TextBox::compileProg()
{
    const char* vShader = "#version 330 core\n"
        "layout (location = 0) in vec2 aPos;\n"
        "layout (location = 1) in vec2 aTexCoords;\n"
        "out vec2 TexCoords;\n"
        "uniform mat4 orthoMatrix;\n"
        "void main()\n"
        "{\n"
        "   gl_Position = orthoMatrix * vec4(aPos, 0.0, 1.0);\n"
        "   TexCoords = aTexCoords;\n"
        "}\0";

    const char* fShader = "#version 330 core\n"
        "out vec4 FragColor;\n"
        "in vec2 TexCoords;\n"
        "uniform sampler2D fontTexture;\n"
        "uniform vec4 bgColor;\n"
        "uniform vec4 textColor;\n"
        "uniform bool isBg;\n"
        "void main()\n"
        "{\n"
        "   if (isBg) {\n"
        "       FragColor = bgColor;\n"
        "   } else {\n"
        "       float a = texture(fontTexture, TexCoords).r;\n"
        "       if (a < 0.1) discard;\n"
        "       FragColor = textColor;\n"
        "   }\n"
        "}\n\0";
    
    unsigned int vs = glCreateShader(GL_VERTEX_SHADER);
    glShaderSource(vs, 1, &vShader, NULL);
    glCompileShader(vs);

    unsigned int fs = glCreateShader(GL_FRAGMENT_SHADER);
    glShaderSource(fs, 1, &fShader, NULL);
    glCompileShader(fs);

    _shaderProgram = glCreateProgram();
    glAttachShader(_shaderProgram, vs);
    glAttachShader(_shaderProgram, fs);
    glLinkProgram(_shaderProgram);

    glDeleteShader(vs);
    glDeleteShader(fs);
}

void TextBox::setText(const std::string& text)
{
    _text = text;
}

void TextBox::draw(const glm::mat4& orthoMatrix)
{
    glDisable(GL_DEPTH_TEST);
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

    glUseProgram(_shaderProgram);
    glUniformMatrix4fv(glGetUniformLocation(_shaderProgram, "orthoMatrix"), 1, GL_FALSE, &orthoMatrix[0][0]);

    glUniform1i(glGetUniformLocation(_shaderProgram, "isBg"), 1);
    glUniform4f(glGetUniformLocation(_shaderProgram, "bgColor"), _color.r, _color.g, _color.b, 0.85f);
    
    float bg[24] = {
        getXpos(),          getYpos(),           0.0f, 0.0f,
        getXpos() + _width, getYpos(),           1.0f, 0.0f,
        getXpos() + _width, getYpos() + _height, 1.0f, 1.0f,
        getXpos(),          getYpos(),           0.0f, 0.0f,
        getXpos() + _width, getYpos() + _height, 1.0f, 1.0f,
        getXpos(),          getYpos() + _height, 0.0f, 1.0f
    };

    glBindBuffer(GL_ARRAY_BUFFER, _VBO);
    glBufferSubData(GL_ARRAY_BUFFER, 0, sizeof(bg), bg);
    glBindVertexArray(_VAO);
    glDrawArrays(GL_TRIANGLES, 0, 6);

    glUniform1i(glGetUniformLocation(_shaderProgram, "isBg"), 0);
    glUniform4f(glGetUniformLocation(_shaderProgram, "textColor"), 0.0f, 1.0f, 0.0f, 1.0f);
    
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, _textureID);
    glUniform1i(glGetUniformLocation(_shaderProgram, "fontTexture"), 0);

    std::vector<float> tVerts;
    float cx = getXpos() + 10.0f;       
    float cy = getYpos() + _height - 24.0f; 
    float scale = 16.0f;              

    for (char c : _text) 
    {
        if (c == '\n') 
        {
            cx = getXpos() + 10.0f;
            cy -= scale * 1.5f; 
            continue;
        }

        int idx = c - 32;
        if (idx < 0 || idx >= 96) idx = 31;

        float tx = (idx % 16) / 16.0f;
        float ty = (idx / 16) / 6.0f;
        float tw = 1.0f / 16.0f;
        float th = 1.0f / 6.0f;

        float tyTop = 1.0f - ty;
        float tyBot = tyTop - th;

        float v[] = {
            cx,         cy,         tx,      tyBot,
            cx + scale, cy,         tx + tw, tyBot,
            cx + scale, cy + scale, tx + tw, tyTop,

            cx,         cy,         tx,      tyBot,
            cx + scale, cy + scale, tx + tw, tyTop,
            cx,         cy + scale, tx,      tyTop
        };

        tVerts.insert(tVerts.end(), v, v + 24);
        cx += scale * 0.8f; 
    }

    if (!tVerts.empty()) 
    {
        glBufferSubData(GL_ARRAY_BUFFER, 0, tVerts.size() * sizeof(float), tVerts.data());
        glDrawArrays(GL_TRIANGLES, 0, tVerts.size() / 4);
    }

    glBindVertexArray(0);
    glDisable(GL_BLEND);
    glEnable(GL_DEPTH_TEST); 
}

